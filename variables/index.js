// Rules for variable names:
// 1. Cannot be a reserved keyword
// 2. Should be meaningful
// 3. Cannot start with a number (1name)
// 4. Cannot contain a space or hyphen (-)
// 5. Are case-sensitive
// 6. Should use camelCase
// 7. One variable per line

// let - use for variables that change (reassign)
let firstName = 'Jean';
console.log(firstName);

// const - use for variables that do not change
const interestRate = 0.3;
console.log(interestRate);
